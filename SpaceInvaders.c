void DrawScreen()
{
	int i;
	//Print Player
	ST7735_DrawBitmap(Player.x,Player.y,Player.image,Player.w,Player.h);
	
	//Print Bunkers
	for(i=0;i<3;i++)
	{
		if(Friendly[i].life==alive)
		{
			ST7735_DrawBitmap(Friendly[i].x,Friendly[i].y,Friendly[i].image,Friendly[i].w,Friendly[i].h);
		}
		else if(Friendly[i].life==neardead)
		{
			Friendly[i].life=dead;
			ST7735_FillRect(Friendly[i].x,Friendly[i].y-Friendly[i].h,Friendly[i].w,Friendly[i].h,0x00);
		}
	}
	
	//print enemy ships
	for(i=0;i<18;i++)
	{
		if(Enemy[i].life==alive)
		{
			ST7735_DrawBitmap(Enemy[i].x,Enemy[i].y,Enemy[i].image,Enemy[i].w,Enemy[i].h);
		}
		else if(Enemy[i].life==neardead)
		{
			Enemy[i].life=dead;
			ST7735_FillRect(Enemy[i].x,Enemy[i].y-Enemy[i].h,Enemy[i].w,Enemy[i].h,0x00);
		}
	}
}

int collisionDetect(STyp one, STyp two)
{
	if((one.x+one.w)>=two.x && one.x<(two.x+two.w) && (one.y-one.h)<two.y && one.y>(two.y+two.h))
	{
		return 1;
	}
	return 0;
}


int main(void)
{
	HardWareInit();
	SoftWareInit();
	while(1)
	{
		DrawScreen();
	}
}	


//SysTick handles sprite movement
void SysTick_Handler(void)
{
	GPIO_PORTF_DATA_R^= 0x01;
	int i;
	if(Ammo.life==alive)
	{
		Ammo.y+=3;
	}
	for(i=0;i<18;i++)
	{
		Enemy[i].x+=EnemyOffset;
		if(Enemy[i].life==alive && Enemy[i].x+Enemy[i].w>=128)
		{
			Enemy[i].y+=2;
			EnemyOffset*=-1;
			if(collisionDetect(Enemy[i],Ammo))
			{
				Enemy[i].life=neardead;
			}
		}
	}	
	GPIO_PORTF_DATA_R^= 0x01;
}


